#include<stdio.h>

int main(void)
{
    
    printf("The size of int is: %lld\n", sizeof(int));
    printf("The size of long int is: %lld\n", sizeof(long int));
    printf("The size of long long int is: %lld\n", sizeof(long long int));
    
    printf("The size of unsigned int is: %lld\n", sizeof(unsigned int));
    printf("The size of unsigned long int is: %lld\n", sizeof(unsigned long int));
    printf("The size of unsigned long long int is: %lld\n", sizeof(unsigned long long int));




    return 0;
}